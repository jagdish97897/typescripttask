// Tuples are similar to arrays but have a fixed length and can have elements with different data types at different positions.

// Declare a tuple with two elements of different types
let person: [string, number] = ["John Doe", 30];

// Access elements by index
console.log(person[0]);
console.log(person[1]); 

// Update elements
person[0] = "ram";
person[1] = 25;

console.log(person[0])
console.log(person[1])
